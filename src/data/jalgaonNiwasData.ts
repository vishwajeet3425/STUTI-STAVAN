function marathiToEnglish(text: string): string {
  // Transliterate common words & names
  let result = text
    .replace(/महानुभाव/g, 'Mahanubhav')
    .replace(/आश्रम/g, 'Ashram')
    .replace(/मंदिर/g, 'Mandir')
    .replace(/श्रीकृष्ण/g, 'Shree Krishna')
    .replace(/प\.पू\.म\./g, 'P.P.M.')
    .replace(/प\.पू\./g, 'P.P.')
    .replace(/प\.प\./g, 'P.P.')
    .replace(/त\./g, 'T.')
    .replace(/श्री/g, 'Shree')
    .replace(/बाबा/g, 'baba')
    .replace(/दादा/g, 'dada')
    .replace(/बुवा/g, 'buva')
    .replace(/शास्त्री/g, 'Shastri')
    .replace(/श्रीमंत/g, 'Shrimant')
    .replace(/भक्त निवास/g, 'Bhakt Niwas')
    .replace(/संस्कृत महाविद्यालय/g, 'Sanskrit Mahavidyalaya')
    .replace(/गुरुकुल/g, 'Gurukul')
    .replace(/देवस्थान/g, 'Devasthan')
    .replace(/संस्था/g, 'Sanstha')
    .replace(/शहर/g, 'City')
    .replace(/गट/g, 'Group');

  // Convert marathi numbers or specific names
  return result;
}

